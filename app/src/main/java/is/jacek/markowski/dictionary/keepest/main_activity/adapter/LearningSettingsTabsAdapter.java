package is.jacek.markowski.dictionary.keepest.main_activity.adapter;

import android.support.v4.app.Fragment;
import android.support.v4.app.FragmentManager;
import android.support.v4.app.FragmentPagerAdapter;

import java.util.List;

/**
 * Created by jacek on 8/5/17.
 */

public class LearningSettingsTabsAdapter extends FragmentPagerAdapter {

    private List<TabFragmentTitle> mFragments;

    public LearningSettingsTabsAdapter(FragmentManager manager, List<TabFragmentTitle> fragments) {
        super(manager);
        mFragments = fragments;
    }

    @Override
    public Fragment getItem(int position) {
        return (Fragment) mFragments.get(position);
    }

    @Override
    public int getCount() {
        return mFragments.size();
    }

    @Override
    public CharSequence getPageTitle(int position) {
        return mFragments.get(position).getTitle();
    }

    public interface TabFragmentTitle {
        String getTitle();
    }

}

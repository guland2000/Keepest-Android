package is.jacek.markowski.dictionary.keepest.main_activity.fragment;


import android.os.Bundle;
import android.support.v4.app.Fragment;
import android.support.v7.widget.Toolbar;
import android.view.LayoutInflater;
import android.view.Menu;
import android.view.MenuInflater;
import android.view.View;
import android.view.ViewGroup;
import android.widget.Button;

import is.jacek.markowski.dictionary.keepest.R;
import is.jacek.markowski.dictionary.keepest.main_activity.MainActivity;


public class EmptyFragment extends Fragment {
    public static final String TAG = "emptyFragment";
    private Button mButton;

    public EmptyFragment() {
        // Required empty public constructor
    }


    @Override
    public View onCreateView(LayoutInflater inflater, ViewGroup container,
                             Bundle savedInstanceState) {
        final View root = inflater.inflate(R.layout.fragment_empty, container, false);
        MainActivity activity = (MainActivity) getActivity();
        activity.setAsLastFragment(TAG);
        Toolbar toolbarFragment = getActivity().findViewById(R.id.toolbar);
        activity.setToolbar(toolbarFragment, getString(R.string.app_name));
        mButton = root.findViewById(R.id.bt_create_first_dictionary);
        return root;
    }

    @Override
    public void onStart() {
        super.onStart();
    }

    @Override
    public void onDestroy() {
        super.onDestroy();
    }

    @Override
    public void onCreateOptionsMenu(Menu menu, MenuInflater inflater) {
        super.onCreateOptionsMenu(menu, inflater);
        inflater.inflate(R.menu.word, menu);
    }
}

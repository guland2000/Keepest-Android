package is.jacek.markowski.dictionary.keepest.main_activity.util;

import net.lingala.zip4j.core.ZipFile;
import net.lingala.zip4j.exception.ZipException;
import net.lingala.zip4j.model.ZipParameters;
import net.lingala.zip4j.util.Zip4jConstants;

import java.io.File;

/**
 * Created by jacek on 9/30/17.
 */

public class ZipCompression {
    public static void compress(File zipFile, File fileToCompress) {
        try {
            ZipParameters parameters = new ZipParameters();
            parameters.setCompressionMethod(Zip4jConstants.COMP_DEFLATE);
            parameters.setCompressionLevel(Zip4jConstants.DEFLATE_LEVEL_FASTEST);
            ZipFile archive = new ZipFile(zipFile);
            archive.addFile(fileToCompress, parameters);
        } catch (ZipException e) {
            e.printStackTrace();
        } finally {
        }
    }

    public static void extract(File source, String destination) {
        try {
            ZipFile zipFile = new ZipFile(source);
            zipFile.extractAll(destination);
        } catch (ZipException e) {
            e.printStackTrace();
        }
    }

}
